public final void myconnection () 
{
    try{
        String query = "{call showQ5()}";   
        dbConnection = DriverManager.getConnection(url, username, passwd);
        PreparedStatement stmt = dbConnection.prepareCall(query);
        DefaultTableModel tm = (DefaultTableModel)jTable1.getModel();
        tm.setRowCount(0);
        ResultSet rs = stmt.executeQuery();

        while(rs.next())
        {
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            Object[] row = { rs.getString(1),rs.getString(2),rs.getFloat(3) };
            model.addRow(row);
        }
    }
    catch(Exception e){

        JOptionPane.showMessageDialog(null,"Η Εμφάνιση δεν πραγματοποιήθηκε. "+e);
        try {
            dbConnection.close();
            this.dispose();
        } catch(SQLException ex) {
            System.out.println("\n -- SQL Exception --- \n");
            while(ex != null) {
                System.out.println("Message: " + ex.getMessage());
                ex = ex.getNextException();
            }
        }
        System.exit(0);
    }
}  
